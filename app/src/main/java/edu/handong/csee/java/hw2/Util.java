package edu.handong.csee.java.hw2;
/**
 * This is a Class Util which enables String modification to uppercase
 */
public class Util {
    /**
     * 
     * @param stringValue
     * @return stringValue.toUpperCase()
     * This is a method getUppercaseString which returns a modified string in the uppercase
     */
    static String getUppercaseString(String stringValue) {
        return stringValue.toUpperCase();
    }
    
}
